// String properties and methods in  Java Script

// Uppercase
let flower = "rose";
console.log(flower.toUpperCase());

//  Lowercase
let fruit = "MANGO";
console.log(fruit.toLowerCase());

// Slice first - index second - position

let car = "Tigor";
console.log(car.slice(2,4)); // go

let car1 = "Indigo"; 
console.log(car1.slice(2));

// Replace
let name5 = "Saket kumar";
console.log(name5.replace("kumar","Shukla"))


// Concat
let name6 = "Gaurav";
let name7 = "Pandey";
console.log(name6.concat(name7))

// Trim
let friend2 = "    Saket       ";
console.log(friend2)
console.log(friend2.length);

console.log(friend2.trim())
console.log(friend2.length);

// Trim end
let vegetables = "Potato        ";
console.log(vegetables.trimEnd());

// Trim Start
let gov = "       PM Modi";
console.log(gov.trimStart());